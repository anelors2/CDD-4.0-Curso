package Aula01;

public class Exemplo01 {
	public static void main(String[] args) {
		//final ano=2024 constante em Java	
		//String em java é classe
		//int - inteiro
		//double - float
		//contador++ - operador unário
		
		int x =10;
		System.out.println(x++); //x++ - adiciona 1 após o print (10+1=11)
		System.out.println(++x); //x++ - adiciona 1 após o print (11+1=12)
		System.out.println(x--); //x++ - subtrai 1 após o print (12-1=11)
		System.out.println(--x); //x++ - subtrai 1 após o print (11+1=10)
		
		
	}
}
